//maths is a default library in js
console.log(Math)
console.log(Math.abs(-5)) //gives absolute value i.e., 5

console.log(Math.round(5.6)) //rounds off to nearest 10

console.log(Math.ceil(3.1)) //rounds off to upper limit 

console.log(Math.floor(3.8)) //rounds off to lower limit (greatest integer function)

console.log(Math.sqrt(36))

console.log(Math.min(3,5,5,6,24,1))

console.log(Math.random()) //gives a numerical value between 0 and 1


console.log(typeof Math) // Math is an object, not a function or class
console.log(Math.PI) // Accessing a property of the Math object
console.log(Math.E) // Euler's number, another property of the Math object